package org.eclipse.tractusx.edc.mxd.federatedcatalog;

import org.eclipse.edc.crawler.spi.TargetNodeFilter;
import org.eclipse.edc.runtime.metamodel.annotation.Provider;
import org.eclipse.edc.spi.system.ServiceExtension;
import org.eclipse.edc.spi.system.ServiceExtensionContext;

public class MxdNodeFilterExtension implements ServiceExtension {

    @Provider
    public TargetNodeFilter mxdNodeFilter(ServiceExtensionContext context) {
        return targetNode -> !context.getComponentId().equals(targetNode.name());
    }
}
